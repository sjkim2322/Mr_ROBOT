package com.seongJIn.Robot.Vo;

public class FormatVo {
	
	private String url;
	
	private String rule;
	
	

	
	@Override
	public String toString() {
		return "FormatVo [url=" + url + ", rule=" + rule + "]";
	}

	public String getUrl() {
		return url;
	}

	public String getRule() {
		return rule;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public void setRule(String rule) {
		this.rule = rule;
	}
	
	

}
